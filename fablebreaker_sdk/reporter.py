"""
FableBreaker Report Generator — produces structured reports.
"""

from __future__ import annotations

import json
from datetime import datetime, timezone
from typing import Any


class ReportGenerator:
    """
    Generates structured reports from FableBreaker scan results.

    Supports JSON, markdown, and summary formats.
    """

    @staticmethod
    def to_json(results: dict[str, Any], indent: int = 2) -> str:
        """Convert scan results to formatted JSON."""
        return json.dumps(results, indent=indent, default=str)

    @staticmethod
    def to_markdown(results: dict[str, Any], title: str = "FableBreaker Scan Report") -> str:
        """Convert scan results to a markdown report."""
        lines = [
            f"# {title}",
            "",
            f"**Generated:** {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M:%S UTC')}",
            "**SDK Version:** 1.0.0",
            "",
            "---",
            "",
        ]

        # Summary section
        summary = results.get("summary", {})
        if summary:
            lines.append("## Summary")
            lines.append("")
            lines.append(f"- **File:** {summary.get('file', 'N/A')}")
            lines.append(f"- **Language:** {summary.get('language', 'N/A')}")
            lines.append(f"- **Total Issues:** {summary.get('total_issues_across_all_skills', 0)}")
            lines.append(f"- **Skills Invoked:** {', '.join(summary.get('skills_invoked', []))}")
            lines.append("")

        # Code Review section
        code_review = results.get("code_review", {})
        if code_review:
            output = code_review.get("output", {})
            lines.append("## Code Review")
            lines.append("")
            lines.append(f"- **Issues Found:** {output.get('total_issues', 0)}")
            lines.append(f"- **Lines Reviewed:** {output.get('lines_reviewed', 0)}")
            findings = output.get("findings", [])
            if findings:
                lines.append("")
                lines.append("| Category | Severity | Finding |")
                lines.append("|----------|----------|---------|")
                for f in findings[:10]:
                    lines.append(
                        f"| {f.get('category', '')} | {f.get('severity', '')} | {f.get('finding', '')} |"
                    )
            lines.append("")

        # Security section
        security = results.get("security", {})
        if security:
            output = security.get("output", {})
            lines.append("## Security")
            lines.append("")
            lines.append(f"- **Risk Score:** {output.get('risk_score', 0)}/10")
            lines.append(f"- **Vulnerabilities:** {output.get('total_findings', 0)}")
            lines.append(f"- **Critical:** {output.get('critical_count', 0)}")
            vulns = output.get("vulnerabilities", [])
            if vulns:
                lines.append("")
                for v in vulns[:5]:
                    lines.append(f"- ⚠️ **{v.get('type', '')}** ({v.get('severity', '')}): {v.get('description', '')}")
            lines.append("")

        # Refactoring section
        refactoring = results.get("refactoring", {})
        if refactoring:
            output = refactoring.get("output", {})
            lines.append("## Refactoring Opportunities")
            lines.append("")
            lines.append(f"- **Opportunities:** {output.get('total_opportunities', 0)}")
            lines.append(f"- **Improvement:** {output.get('estimated_improvement', 'N/A')}")
            lines.append("")

        # Documentation section
        documentation = results.get("documentation", {})
        if documentation:
            output = documentation.get("output", {})
            lines.append("## Documentation")
            lines.append("")
            lines.append(f"- **Score:** {output.get('documentation_score', 0)}%")
            lines.append(f"- **Grade:** {output.get('quality_grade', 'N/A')}")
            lines.append("")

        # Self-Analysis section
        self_analysis = results.get("self_analysis", {})
        if self_analysis:
            output = self_analysis.get("output", {})
            report = output.get("self_analysis_report", {})
            lines.append("## Self-Analysis")
            lines.append("")
            lines.append(f"- **Findings:** {report.get('total_findings', 0)}")
            lines.append(f"- **Verdict:** {output.get('verdict', 'N/A')}")
            meta = report.get("meta_analysis", {})
            if meta:
                lines.append(f"- **Self-Awareness Score:** {meta.get('self_awareness_score', 0)}")
                lines.append(f"- **Blind Spot Risk:** {meta.get('blind_spot_risk', 'N/A')}")
            lines.append("")

        lines.append("---")
        lines.append("")
        lines.append("*Generated by FableBreaker AI SDK v1.0.0 — ItsNotAI LABS*")

        return "\n".join(lines)

    @staticmethod
    def to_summary(results: dict[str, Any]) -> str:
        """Generate a one-line summary of results."""
        summary = results.get("summary", {})
        total = summary.get("total_issues_across_all_skills", 0)
        filename = summary.get("file", "unknown")
        return f"[FableBreaker] {filename}: {total} issues found across all skills"
