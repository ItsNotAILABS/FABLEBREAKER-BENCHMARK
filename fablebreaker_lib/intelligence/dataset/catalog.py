"""
Complete catalog of AI benchmarks across all domains.

This is the training knowledge that Fablebreaker Intelligence ingests.
Benchmarks are data — Fablebreaker is the intelligence that evaluates them.
"""

from __future__ import annotations

from typing import Any


def _entry(
    name: str,
    domain: str,
    category: str,
    description: str,
    metrics: list[str],
    year: int,
    organization: str,
    task_types: list[str],
    scale: str,
    url: str = "",
    limitations: list[str] | None = None,
    gaming_vectors: list[str] | None = None,
    correctness_requirements: list[str] | None = None,
    tags: list[str] | None = None,
) -> dict[str, Any]:
    return {
        "name": name,
        "domain": domain,
        "category": category,
        "description": description,
        "metrics": metrics,
        "year": year,
        "organization": organization,
        "task_types": task_types,
        "scale": scale,
        "url": url,
        "limitations": limitations or [],
        "gaming_vectors": gaming_vectors or [],
        "correctness_requirements": correctness_requirements or [],
        "tags": tags or [],
    }


