"""
Behavioral monitoring and drift detection.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from datetime import datetime, timezone
from enum import Enum
from typing import Any


class AlertSeverity(Enum):
    """Severity of monitoring alerts."""
    INFO = "info"
    WARNING = "warning"
    CRITICAL = "critical"
    EMERGENCY = "emergency"


@dataclass
class DriftMetric:
    """A metric tracking behavioral drift over time."""
    name: str
    current_value: float
    baseline_value: float
    threshold: float
    unit: str = ""

    @property
    def drift_pct(self) -> float:
        if self.baseline_value == 0:
            return 0.0
        return abs(self.current_value - self.baseline_value) / abs(self.baseline_value) * 100

    @property
    def exceeded(self) -> bool:
        return self.drift_pct > self.threshold

    def to_dict(self) -> dict[str, Any]:
        return {
            "name": self.name,
            "current_value": self.current_value,
            "baseline_value": self.baseline_value,
            "drift_pct": self.drift_pct,
            "threshold": self.threshold,
            "exceeded": self.exceeded,
            "unit": self.unit,
        }


@dataclass
class MonitoringAlert:
    """An alert generated by the behavioral monitor."""
    id: str
    severity: AlertSeverity
    title: str
    description: str
    metric: DriftMetric | None = None
    candidate_name: str = ""
    timestamp_utc: str = field(default_factory=lambda: datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ"))
    acknowledged: bool = False

    def acknowledge(self) -> None:
        self.acknowledged = True

    def to_dict(self) -> dict[str, Any]:
        return {
            "id": self.id,
            "severity": self.severity.value,
            "title": self.title,
            "description": self.description,
            "metric": self.metric.to_dict() if self.metric else None,
            "candidate_name": self.candidate_name,
            "timestamp_utc": self.timestamp_utc,
            "acknowledged": self.acknowledged,
        }


class BehaviorMonitor:
    """Monitors AI system behavior over time and detects drift."""

    def __init__(self) -> None:
        self._baselines: dict[str, dict[str, float]] = {}
        self._current: dict[str, dict[str, float]] = {}
        self._alerts: list[MonitoringAlert] = []
        self._thresholds: dict[str, float] = {
            "speedup": 20.0,  # 20% drift threshold
            "correctness": 1.0,  # 1% drift (very sensitive)
            "latency_p95": 50.0,  # 50% drift
            "failure_rate": 5.0,  # 5% absolute
        }

    def set_baseline(self, candidate_name: str, metrics: dict[str, float]) -> None:
        """Set baseline metrics for a candidate."""
        self._baselines[candidate_name] = dict(metrics)

    def record_metrics(self, candidate_name: str, metrics: dict[str, float]) -> list[MonitoringAlert]:
        """Record current metrics and check for drift."""
        self._current[candidate_name] = dict(metrics)
        alerts: list[MonitoringAlert] = []

        baseline = self._baselines.get(candidate_name, {})
        if not baseline:
            return alerts

        for metric_name, current_val in metrics.items():
            baseline_val = baseline.get(metric_name)
            if baseline_val is None:
                continue

            threshold = self._thresholds.get(metric_name, 25.0)
            drift = DriftMetric(
                name=metric_name,
                current_value=current_val,
                baseline_value=baseline_val,
                threshold=threshold,
            )

            if drift.exceeded:
                severity = AlertSeverity.WARNING
                if drift.drift_pct > threshold * 2:
                    severity = AlertSeverity.CRITICAL
                if metric_name == "correctness" and drift.drift_pct > 0:
                    severity = AlertSeverity.EMERGENCY

                alert = MonitoringAlert(
                    id=f"MON-{len(self._alerts) + 1:04d}",
                    severity=severity,
                    title=f"Behavioral drift detected: {metric_name}",
                    description=f"{metric_name} drifted {drift.drift_pct:.1f}% from baseline (threshold: {threshold}%)",
                    metric=drift,
                    candidate_name=candidate_name,
                )
                alerts.append(alert)
                self._alerts.append(alert)

        return alerts

    def set_threshold(self, metric_name: str, threshold: float) -> None:
        self._thresholds[metric_name] = threshold

    @property
    def active_alerts(self) -> list[MonitoringAlert]:
        return [a for a in self._alerts if not a.acknowledged]

    @property
    def all_alerts(self) -> list[MonitoringAlert]:
        return list(self._alerts)

    def summary(self) -> dict[str, Any]:
        return {
            "tracked_candidates": len(self._baselines),
            "total_alerts": len(self._alerts),
            "active_alerts": len(self.active_alerts),
            "thresholds": dict(self._thresholds),
        }
